# Board43 Sample: MIDI Song Playback (buzzer)
#
# Plays a monophonic reduction of xky-k-gr.mid on the buzzer.
# The source MIDI is polyphonic; the Trumpets (track 1),
# Flute Melody (track 2) and Tuba! (track 4) tracks were merged
# into a single line — when more than one of those tracks is
# sounding, the lowest active pitch wins. Only bars 2-51 are
# encoded (the section that repeats in the original).
#
#   SW3: Pause / Resume
#   SW4: Volume down
#   SW5: Volume up
#
# "Volume" here is the PWM duty cycle (0..50). 50% is a full
# square wave (loudest); smaller values narrow the pulse so the
# same frequency sounds quieter.
#
# Features: Switches (GPIO) + Buzzer (PWM)

