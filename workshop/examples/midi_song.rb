POLL_MS = 20

state = {
  buzzer: buzzer,
  pause_btn: pause_btn,
  vdown_btn: vdown_btn,
  vup_btn: vup_btn,
  duty: DEFAULT_DUTY,
  prev_vdown: false,
  prev_vup: false
}

def hex4(bytes, i)
  v = 0
  j = 0
  while j < 4
    b = bytes[i + j]
    # '0'..'9' -> 48..57, 'a'..'f' -> 97..102
    c = b >= 97 ? b - 87 : b - 48
    v = (v << 4) | c
    j += 1
  end
  v
end

def adjust_volume(state, delta)
  new_duty = state[:duty] + delta
  new_duty = MIN_DUTY if new_duty < MIN_DUTY
  new_duty = MAX_DUTY if new_duty > MAX_DUTY
  return if new_duty == state[:duty]
  state[:duty] = new_duty
  state[:buzzer].duty(new_duty)
end

def poll_volume(state)
  vdown = state[:vdown_btn].low?
  vup   = state[:vup_btn].low?
  adjust_volume(state, -DUTY_STEP) if vdown && !state[:prev_vdown]
  adjust_volume(state,  DUTY_STEP) if vup   && !state[:prev_vup]
  state[:prev_vdown] = vdown
  state[:prev_vup]   = vup
end

def wait_if_paused(state, playing_freq)
  return unless state[:pause_btn].low?
  state[:buzzer].frequency(0)
  sleep_ms 200
  while state[:pause_btn].high?
    poll_volume(state)
    sleep_ms POLL_MS
  end
  sleep_ms 200
  state[:buzzer].frequency(playing_freq)
end

def play_note(state, freq, dur_ms)
  state[:buzzer].frequency(freq)
  remaining = dur_ms
  while remaining > 0
    step = remaining < POLL_MS ? remaining : POLL_MS
    sleep_ms step
    remaining -= step
    poll_volume(state)
    wait_if_paused(state, freq)
  end
end

bytes = NOTES.bytes
total = bytes.length

loop do
  i = 0
  while i < total
    freq = hex4(bytes, i)
    dur  = hex4(bytes, i + 4)
    play_note(state, freq, dur)
    i += 8
  end
  buzzer.frequency(0)
  sleep_ms 500
end
