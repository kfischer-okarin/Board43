FRAME_DELAY_MS = 220
POLL_MS        = 20

def draw_frame(led, frame)
  bytes = frame.bytes
  i = 0
  while i < 256
    b = bytes[i]
    # '0'..'9' -> 48..57, 'a'..'f' -> 97..102
    idx = b >= 97 ? b - 87 : b - 48
    rgb = PALETTE[idx]
    led.set_rgb(i, rgb[0], rgb[1], rgb[2])
    i += 1
  end
  led.show
end

frame_index = 0
paused = false
prev_btn_low = false

draw_frame(led, FRAMES[frame_index])
elapsed = 0

loop do
  btn_low = pause_btn.low?
  if btn_low && !prev_btn_low
    paused = !paused
  end
  prev_btn_low = btn_low

  unless paused
    elapsed += POLL_MS
    if elapsed >= FRAME_DELAY_MS
      frame_index = (frame_index + 1) % FRAMES.length
      draw_frame(led, FRAMES[frame_index])
      elapsed = 0
    end
  end

  sleep_ms POLL_MS
end
