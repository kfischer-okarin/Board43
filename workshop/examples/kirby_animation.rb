# Board43 Sample: Kirby Animation
#
# Plays an 8-frame Kirby sprite animation on the 16x16 LED matrix.
# Frames were extracted from the third row of a 16x16 sprite sheet;
# the magenta background pixels are rendered as unlit LEDs.
#
#   SW3: Pause / Resume
#
# Features: Switches (GPIO) + WS2812 16x16 matrix

require 'ws2812-plus'
require 'gpio'

