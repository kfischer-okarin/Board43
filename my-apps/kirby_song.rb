state = {
  led: led,
  buzzer: buzzer,
  song_btn: song_btn,
  anim_btn: anim_btn,
  vdown_btn: vdown_btn,
  vup_btn: vup_btn,
  duty: DEFAULT_DUTY,
  current_freq: 0,
  frame_index: 0,
  frame_elapsed: FRAME_DELAY_MS,  # force draw on first tick
  song_paused: false,
  anim_paused: false,
  prev_song: false,
  prev_anim: false,
  prev_vdown: false,
  prev_vup: false
}

def play_song(state, notes_bytes)
  total = notes_bytes.length
  i = 0
  while i < total
    freq = hex4(notes_bytes, i)
    dur  = hex4(notes_bytes, i + 4)
    play_note(state, freq, dur)
    i += 8
  end
end

def play_note(state, freq, dur_ms)
  state[:current_freq] = freq
  state[:buzzer].frequency(freq) unless state[:song_paused]
  remaining = dur_ms
  while remaining > 0
    step = remaining < POLL_MS ? remaining : POLL_MS
    sleep_ms step
    remaining -= step
    tick(state, step)
    handle_song_pause(state)
  end
end

def handle_song_pause(state)
  return unless state[:song_paused]
  state[:buzzer].frequency(0)
  while state[:song_paused]
    tick(state, POLL_MS)
    sleep_ms POLL_MS
  end
  state[:buzzer].frequency(state[:current_freq])
end

def tick(state, ms)
  poll_controls(state)
  advance_animation(state, ms)
end

def advance_animation(state, ms)
  return if state[:anim_paused]
  state[:frame_elapsed] += ms
  return if state[:frame_elapsed] < FRAME_DELAY_MS
  state[:frame_index] = (state[:frame_index] + 1) % FRAMES.length
  draw_frame(state[:led], FRAMES[state[:frame_index]])
  state[:frame_elapsed] = 0
end

def poll_controls(state)
  song  = state[:song_btn].low?
  anim  = state[:anim_btn].low?
  vdown = state[:vdown_btn].low?
  vup   = state[:vup_btn].low?

  state[:song_paused] = !state[:song_paused] if song  && !state[:prev_song]
  state[:anim_paused] = !state[:anim_paused] if anim  && !state[:prev_anim]
  adjust_volume(state, -DUTY_STEP)           if vdown && !state[:prev_vdown]
  adjust_volume(state,  DUTY_STEP)           if vup   && !state[:prev_vup]

  state[:prev_song]  = song
  state[:prev_anim]  = anim
  state[:prev_vdown] = vdown
  state[:prev_vup]   = vup
end

def adjust_volume(state, delta)
  new_duty = state[:duty] + delta
  new_duty = MIN_DUTY if new_duty < MIN_DUTY
  new_duty = MAX_DUTY if new_duty > MAX_DUTY
  return if new_duty == state[:duty]
  state[:duty] = new_duty
  state[:buzzer].duty(new_duty)
end

def draw_frame(led, frame)
  bytes = frame.bytes
  i = 0
  while i < 256
    b = bytes[i]
    # '0'..'9' -> 48..57, 'a'..'f' -> 97..102
    idx = b >= 97 ? b - 87 : b - 48
    rgb = PALETTE[idx]
    led.set_rgb(i, rgb[0], rgb[1], rgb[2])
    i += 1
  end
  led.show
end

def hex4(bytes, i)
  v = 0
  j = 0
  while j < 4
    b = bytes[i + j]
    c = b >= 97 ? b - 87 : b - 48
    v = (v << 4) | c
    j += 1
  end
  v
end

notes_bytes = NOTES.bytes

loop do
  play_song(state, notes_bytes)
  buzzer.frequency(0)
  sleep_ms 500
end
