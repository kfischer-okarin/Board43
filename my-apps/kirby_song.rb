# Board43 Sample: Kirby Animation + MIDI Song
#
# Plays the 8-frame Kirby animation on the 16x16 LED matrix while a
# monophonic reduction of xky-k-gr.mid plays on the buzzer.
# The source MIDI is polyphonic; Trumpets (track 1), Flute Melody
# (track 2) and Tuba! (track 4) were merged into a single line —
# when more than one is sounding, the lowest active pitch wins.
# Only bars 2-51 are encoded (the part that repeats in the original).
#
#   SW3: Pause / Resume music
#   SW4: Volume down
#   SW5: Volume up
#   SW6: Pause / Resume animation
#
# "Volume" here is the PWM duty cycle (0..50). 50% is a full square
# wave (loudest); smaller values narrow the pulse so the same
# frequency sounds quieter.
#
# Features: Switches (GPIO) + WS2812 16x16 matrix + Buzzer (PWM)

